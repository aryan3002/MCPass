export default function Sidebar({ tenantName, tenantSlug }: { tenantName: string; tenantSlug: string }) {
  const pathname = usePathname();

  function isActive(href: string) {
    if (href === "/") return pathname === "/";
    return pathname.startsWith(href);
  }

  return (
    <aside className="w-64 bg-gray-900 border-r border-gray-800 flex flex-col flex-shrink-0">
      {/* Logo */}
      <div className="p-6 pb-4">
        <h1 className="text-xl font-bold text-white">MCPaaS</h1>
        <p className="text-sm text-gray-400 mt-1 truncate">{tenantName}</p>
      </div>

      {/* Navigation */}
      <nav className="flex-1 px-3 space-y-0.5">
        {NAV_ITEMS.map((item, idx) => {
          if ("type" in item && item.type === "divider") {
            return <div key={idx} className="h-px bg-gray-800 my-3 mx-2" />;
          }
          const navItem = item as { href: string; label: string; icon: React.ReactNode };
          const active = isActive(navItem.href);
          return (
            <Link
              key={navItem.href}
              href={navItem.href}
              className={`flex items-center gap-3 px-3 py-2 rounded-lg text-sm transition-colors ${
                active
                  ? "bg-gray-800 text-white font-medium"
                  : "text-gray-400 hover:text-gray-200 hover:bg-gray-800/50"
              }`}
            >
              <span className={active ? "text-blue-400" : "text-gray-500"}>{navItem.icon}</span>
              {navItem.label}
              {navItem.label === "Live Feed" && (
                <span className="ml-auto w-1.5 h-1.5 rounded-full bg-green-500 animate-pulse" />
              )}
            </Link>
          );
        })}
      </nav>

      {/* Footer */}
      <div className="p-4 border-t border-gray-800 space-y-2">
        <div className="flex items-center gap-2">
          <div className="w-2 h-2 rounded-full bg-green-500" />
          <span className="text-sm text-gray-400">MCP Server Online</span>
        </div>
        <p className="text-xs text-gray-600 font-mono truncate">/api/{tenantSlug}/mcp</p>
      </div>
    </aside>
  );
}
